let nilai = prompt("masukan nilai anda",0);

if(nilai <= 66){
    document.write("F-")
}else if (nilai <=75){
    document.write("F")
}else if (nilai <=80){
    document.write("E")
}else if (nilai <=85){
    document.write("D")
}else if (nilai <=90){
    document.write("C")
}else if (nilai <=95){   
    document.write("B")
}else if (nilai <=99){
    document.write("A")
}

else (nilai < 0 || nilai > 99)
document.write("error")


